    public void reset() {
        tat = true;
        if (trang_thai.equals(NameConstant.COI_XAY_GIO)) {
            tr_coiXayGio.stop();
        } else if (trang_thai.equals(NameConstant.XE_TAI)) {
            tr_xetai.stop();
        } else if (trang_thai.equals(NameConstant.TONG_HOP_2D)) {
            tr_tongquan.stop();
        }
        drawplace.newImage();
        if (trang_thai_menu.equals(NameConstant.VE_2D)) {
            Coordinate.drawCoordinate2d(drawplace.getImage());
        } else if (trang_thai_menu.equals(NameConstant.VE_3D)) {
            Coordinate.drawCoordinate3D(drawplace.getImage());
        }
        XuLiComponent.resetTextField(txtCao_Hop, txtDai_Hop, txtH_Tru, txtR_DayTru, txtRong_Hop, txtX_CoiXayGio,
                txtX_DiemTraiDuoi, txtX_TamTru, txtX_XeTai1, txtY_CoiXayGio, txtY_DiemTraiDuoi,
                txtY_TamTru, txtY_XeTai1, txtZ_DiemTraiDuoi, txtZ_TamTru
        );
        XuLiComponent.resetLabel(lblDiemCabin, lblDiemCuaSo, lblDiemDauXe, lblDiemDen, lblThan_Coi, lblMai_Coi,
                lblD1_Tam, lblD2_Tam, lblD3_Tam, lblD4_Tam, lblD5_Tam, lblD6_Tam, lblD7_Tam, lblD8_Tam, lblTamCoi, lblDiemXe, lblQuayD1_B1, lblQuayD1_B2, lblQuayD2_B1, lblQuayD2_B2, lblTamB1, lblTamB2, lblDauDayDien, lblDuoiDayDien, lblHopTram1, lblHopTram2, lblHopTram3, lblTiaDien
        );
        XuLiComponent.resetLabel3D(lblDiemHop1, lblDiemHop2, lblDiemHop3, lblDiemHop4, lblDiemHop5, lblDiemHop6, lblDiemHop7, lblDiemHop8,
                lblDiemO_Tru, lblDiemR_Tru, lblDiemO2_Tru, lblDiemR2_Tru);
        drawplace.refreshDrawPlace(drawplace.getImage());

    }

