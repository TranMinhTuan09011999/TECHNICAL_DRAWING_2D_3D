    private void btnDoXeTai1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoXeTai1ActionPerformed
        // TODO add your handling code here:
        if (!XuLiChuoi.kiemTraToaDo(txtX_XeTai1) || !XuLiChuoi.kiemTraToaDo(txtY_XeTai1)) {
            JOptionPane.showMessageDialog(null, MessageConstant.THONGBAO);
        } else {
            jfNhapXeTai.setVisible(false);

            int toado_x = Integer.parseInt(txtX_XeTai1.getText());
            int toado_y = Integer.parseInt(txtY_XeTai1.getText());

            Point p = new Point(toado_x, toado_y);
            xetai = new XeTai(p);
            if (trang_thai.length() > 0) {
                reset();
            }
            tat = false;
            trang_thai = NameConstant.XE_TAI;
            Runnable run = new Runnable() {
                @Override
                public void run() {
                    BufferedImage imageClone = null;
                    int i = 0;
                    while (tat == false) {
                        // xe dụng cac cạnh gioi han thi quay lai
                        Point p1 = Point.convert(xetai.getA());
                        if (p1.getX() == SizeConstant.WIDTH_DRAW_PLACE
                                || p1.getY() == SizeConstant.HEIGHT_DRAW_PLACE - 1
                                || p1.getX() == 0
                                || p1.getY() == 1) {

                            xetai.setA(p);

                        } else {
                            if (i == goc.length) {
                                i = 0;
                            }

                            imageClone = new BufferedImage(drawplace.getImage().getWidth(), drawplace.getImage().getHeight(), drawplace.getImage().getType());
                            imageClone.setData(drawplace.getImage().getData());
                            xetai.drawShape(imageClone, ColorConstant.BLACK_RGB);
                            drawplace.refreshDrawPlace(imageClone);
                            // huong len
                            if (xetai.getHuong().equals(NameConstant.LEN)) {
                                xetai.tinhTien(imageClone, 0, 1);
                                xetai.xoay(imageClone, goc[i]);
                            } //huuong xuong
                            else if (xetai.getHuong().equals(NameConstant.XUONG)) {
                                xetai.tinhTien(imageClone, 0, -1);
                                xetai.xoay(imageClone, goc[i]);
                            } //qua trai
                            else if (xetai.getHuong().equals(NameConstant.TRAI)) {

                                xetai.tinhTien(imageClone, -1, 0);
                                // neu quay xe thi banh xoay thuan theo con khong nguoc lai
                                if (xetai.isQuayxe()) {
                                    xetai.xoay(imageClone, goc[i]);
                                } else {
                                    xetai.xoay(imageClone, -goc[i]);
                                }
                            } else if (xetai.getHuong().equals(NameConstant.PHAI)) {
                                xetai.tinhTien(imageClone, 1, 0);
                                // neu quay xe thi banh xoay thuan theo con khong nguoc lai
                                if (xetai.isQuayxe()) {
                                    xetai.xoay(imageClone, -goc[i]);
                                } else {
                                    xetai.xoay(imageClone, goc[i]);
                                }
                            }

                            i++;

                            Point[] points = XuLiComponent.layDiemCanDienChiTietDiem2D(xetai.getA(), xetai.getDiem_dau(),
                                    xetai.getDiem_hop_dau(), xetai.getDiem_cua_so(), xetai.getDiem_den(),
                                    Point.convert_user(xetai.getX()), Point.convert_user(xetai.getY()),
                                    Point.convert_user(xetai.getD1()), Point.convert_user(xetai.getD2()),
                                    Point.convert_user(xetai.getD3()), Point.convert_user(xetai.getD4()));
                            XuLiComponent.dienChiTietDiem2D(points, lblDiemXe, lblDiemDauXe, lblDiemCabin, lblDiemCuaSo, lblDiemDen,
                                    lblTamB1, lblTamB2, lblQuayD1_B1,
                                    lblQuayD2_B1, lblQuayD1_B2, lblQuayD2_B2);
                            try {
                                Thread.sleep(sliderTocDoXe.getMaximum() - sliderTocDoXe.getValue());
                            } catch (Exception e) {
                            }
                        }
                    }

                }
            };

            tr_xetai = new Thread(run);
            tr_xetai.start();
        }


    }//GEN-LAST:event_btnDoXeTai1ActionPerformed

